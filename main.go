package main

import (
	"github.com/getlantern/systray"
	"github.com/go-vgo/robotgo"
	"log"
	"math/rand"
)

func main() {
	systray.Run(onReady, nil)
}

func onReady() {
	//systray.SetTemplateIcon(icon.Data, icon.Data)
	systray.SetTemplateIcon(MyImg, MyImg)

	mChecked := systray.AddMenuItem("Move Mouse", "Start")
	systray.AddSeparator()
	quitBtn := systray.AddMenuItem("Quit", "quit")

	go func() {
		for {
			select {
			case <-mChecked.ClickedCh:
				if mChecked.Checked() {
					mChecked.Uncheck()
				} else {
					mChecked.Check()
					go moveMouse(mChecked)
				}
			case <-quitBtn.ClickedCh:
				log.Println("Requesting quit")
				systray.Quit()
			}

		}
	}()

}

func moveMouse(item *systray.MenuItem) {
	log.Println("Starting moving mouse")
	for {
		// Random 1 to 10 seconds
		robotgo.MouseSleep = rand.Intn(9000) + 1000
		// Screen size 1280x800
		robotgo.Move(rand.Intn(1280), rand.Intn(800))
		if item.Checked() == false {
			log.Println("Stopping moving mouse")
			break
		}
	}
}

